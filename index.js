function add(a, b) {
  return a + b
}

function subtract(a, b) {
  return a - b
}

function multiply(a, b) {
  return a * b
}

function divide(a, b) {
  return a / b
}

function inc(n) {
  n++
  return n
}

function dec(n) {
  n--
  return n
}

function makeInt(z) {
  parseInt("z", 10)
  return z
}

function preserveDecimal(n) {
  parseFloat('n')
  return n
}
